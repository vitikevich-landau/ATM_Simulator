#pragma once
// ============================================================================
//  ServiceStages.hpp — тематические ЭТАПЫ обслуживания клиента (§4.8: «что
//  сейчас происходит у банкомата»).
//
//  Зачем: время обслуживания в движке — одно число из ServiceTimeProvider, и
//  снаружи видно лишь «идёт обслуживание». Для наблюдателя это скучно и
//  неинформативно. Этот модуль раскладывает интервал обслуживания на понятные
//  человеку шаги: вставил карту -> ввёл PIN -> выбрал операцию -> ... ->
//  забрал карту. Дашборд и команда status показывают текущий шаг.
//
//  Ключевое проектное решение: этап НЕ хранится в движке и НЕ требует
//  дополнительных пробуждений потока обслуживания. Он ВЫЧИСЛЯЕТСЯ из доли
//  уже отработанного времени обслуживания (0..1) чистой функцией
//  serviceStageAt(). Так мы не трогаем тонкий цикл прерываемого ожидания
//  (§6.2): никаких новых точек синхронизации, таймеров и гонок — читатели
//  просто интерпретируют прогресс, который движок и так умеет считать.
// ============================================================================
#include <cstddef>
#include <string>
#include <vector>

#include "atmsim/core/Types.hpp"  // OperationType

namespace atmsim {

// Шаг сценария обслуживания. Часть шагов общая для всех операций (карта, PIN,
// меню, чек), часть — специфична: снятие занято отсчётом купюр, внесение —
// приёмом и пересчётом наличных, запрос баланса — экраном с балансом.
enum class ServiceStage {
    InsertCard,       // клиент вставляет карту
    EnterPin,         // вводит PIN-код
    SelectOperation,  // выбирает операцию в меню
    EnterAmount,      // вводит сумму (снятие/внесение)
    BankRequest,      // банкомат связывается с банком за подтверждением
    CountCash,        // банкомат отсчитывает купюры (снятие)
    DispenseCash,     // клиент забирает наличные из лотка (снятие)
    InsertCash,       // клиент вкладывает купюры в купюроприёмник (внесение)
    VerifyCash,       // банкомат пересчитывает и проверяет купюры (внесение)
    ShowBalance,      // клиент изучает баланс на экране (запрос баланса)
    PrintReceipt,     // банкомат печатает чек
    ReturnCard        // клиент забирает карту
};

// Один шаг плана: какой этап и какую ДОЛЮ полного времени обслуживания он
// занимает. Доли каждого плана в сумме дают ровно 1.0 (проверяется тестом):
// тогда последний этап заканчивается точно к концу обслуживания.
struct StageSlice {
    ServiceStage stage;
    double fraction;
};

// План этапов для операции. Возвращает ссылку на статическую таблицу — план
// фиксирован для типа операции и не зависит от клиента (случайна только полная
// длительность, её разыгрывает ServiceTimeProvider).
const std::vector<StageSlice>& serviceStagePlan(OperationType op);

// Этап по доле отработанного времени обслуживания progress (0..1); значения
// вне диапазона прижимаются к границам. Чистая функция: одна и та же пара
// (op, progress) всегда даёт один и тот же этап.
ServiceStage serviceStageAt(OperationType op, double progress);

// Метка-заглушка для НЕДОСТИЖИМОГО значения этапа (см. to_string). Держим её
// одной константой, чтобы источник и тест (stage_names_are_human_readable)
// сверялись с одним и тем же значением, а не с двумя разъезжающимися литералами.
inline constexpr const char* kUnknownStageLabel = "?";

// Человекочитаемое название этапа для дашборда/статуса — «что делает клиент».
std::string to_string(ServiceStage s);

}  // namespace atmsim
