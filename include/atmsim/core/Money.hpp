#include <cstdint>
#include <string>

namespace atmsim {

// Денежная сумма хранится в МИНИМАЛЬНЫХ единицах валюты (копейки/центы) как
// ЦЕЛОЕ число.
//
// Почему целое, а не float/double (это принципиальное требование §4.2 ТЗ):
//   * double накапливает ошибку округления — после сотен операций «сумма всех
//     счетов + касса» перестанет сходиться до копейки;
//   * с целыми деньги складываются и вычитаются точно, без потери разрядов.
// int64 хватает с колоссальным запасом: ~9.2·10^18 минорных единиц.
using Money = std::int64_t;

// Сколько минимальных единиц в одной «крупной» единице валюты (1 EUR = 100 центов).
// От этой константы зависит форматирование (2 знака после точки).
inline constexpr Money kMinorUnitsPerMajor = 100;

// Форматирует сумму для вывода человеку:
//   50000 -> "500.00",  5 -> "0.05",  0 -> "0.00",  -134000 -> "-1340.00".
// Группировку разрядов (пробелы в "482 350.00") добавим на этапе визуализации (M6).
std::string formatMoney(Money minor);

}  // namespace atmsim
