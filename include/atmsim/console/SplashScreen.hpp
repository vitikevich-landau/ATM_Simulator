#pragma once
// ============================================================================
//  SplashScreen.hpp — экран загрузки «самотест банкомата» (сцена v2).
//
//  Настоящие банкоматы на старте гоняют self-test с чек-листом подсистем —
//  делаем так же: рамка с названием/версией и пункты проверки с РЕАЛЬНЫМИ
//  значениями конфига (касса, журнал операций, поток клиентов, зерно ГСЧ...),
//  спиннер на текущем пункте, зелёное «ГОТОВ К РАБОТЕ» в конце.
//
//  Показывается в начале КАЖДОГО прогона (включая restart) и ДО запуска
//  потоков движка: симуляция стартует только после заставки — первый клиент
//  прогона (его интервал прихода 0.0) не начинает путь «за кадром».
//  Вне TTY (пайп/CI) заставки нет вовсе; любая клавиша пропускает;
//  SIGINT/SIGTERM прерывает немедленно.
//
//  Разделение для тестов: splashChecklist() — ЧИСТАЯ сборка пунктов из
//  конфига (ни ANSI, ни ввода-вывода); showSplash() — единственное место,
//  которое трогает терминал.
// ============================================================================
#include <string>
#include <vector>

#include "atmsim/config/Config.hpp"

namespace atmsim {

// Один пункт самотеста: название и деталь-значение (пустая — без детали).
struct SplashItem {
    std::string name;
    std::string detail;
};

// Пункты самотеста с реальными значениями конфига (без ANSI-кодов).
std::vector<SplashItem> splashChecklist(const Config& cfg);

// Показать заставку (блокирует ~2.5 c; любая клавиша — пропустить). Вне TTY
// или при уже запрошенной остановке — мгновенный no-op.
void showSplash(const Config& cfg);

}  // namespace atmsim
