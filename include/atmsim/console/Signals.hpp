//  Signals.hpp — плавная остановка по SIGINT/SIGTERM (§4.6, §5).
//
//  Ctrl+C не должен «убивать» процесс на середине: обработчик лишь выставляет
//  флаг. На POSIX обработчик ставится без SA_RESTART, поэтому блокирующий
//  getline в консоли прерывается (EINTR) и возвращает управление — дальше main
//  корректно останавливает потоки, сохраняет логи и статистику.
// ============================================================================

namespace atmsim {

// Устанавливает обработчики SIGINT и SIGTERM.
void installSignalHandlers();

// true, если был получен сигнал остановки.
bool shutdownRequested();

}  // namespace atmsim
