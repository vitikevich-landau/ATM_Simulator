#pragma once
// ============================================================================
//  FrameDiffer.hpp — построчный дифф кадров дашборда (этап 5 feature/scene).
//
//  Зачем: полная перерисовка кадра на каждом тике упирается в legacy conhost
//  (~42-50 мс на запись 100x30 с цветом — см. docs/scene_frame_budget.md), а
//  в Windows Terminal просто гоняет лишние килобайты. Между соседними кадрами
//  реально меняются единицы строк (уптайм в шапке, двигающиеся человечки,
//  сменившаяся строка очереди) — вот их и переписываем.
//
//  Модель: хранится предыдущий кадр (vector<string>); diff() возвращает
//  ANSI-пакет «moveTo(строка) + строка + clearToLineEnd» ТОЛЬКО для строк,
//  отличающихся от прошлого кадра. Первый кадр и кадр после invalidate() —
//  полный. Дифф ПО СТРОКАМ, а не по клеткам: таблица дашборда — это готовые
//  ANSI-строки, а не клеточная канва; строчной гранулярности достаточно,
//  чтобы дифф-кадр стоил ~1 мс даже в conhost (сценарий sparse бенчмарка).
//
//  Владение: экземпляр принадлежит render-потоку LiveRenderer (тот же
//  инвариант, что у ScenePresenter). invalidate() извне не зовут — консоль
//  сигналит рендеру атомарным флагом, а тот сам инвалидирует (см. resume()).
//  Чисто строковая логика — юнит-тестируется без терминала.
// ============================================================================
#include <string>
#include <vector>

namespace atmsim {

class FrameDiffer {
public:
    // ANSI-пакет перерисовки кадра lines: только изменившиеся строки (после
    // invalidate()/первого вызова — все). Пустая строка = кадр не изменился,
    // терминал можно не трогать вовсе. Побочный эффект: lines запоминается
    // как «отрисованный» кадр — если пакет в итоге НЕ ушёл в терминал
    // (перехватила пауза), обязательно вызовите invalidate(). Кадр принимаем
    // ПО ЗНАЧЕНИЮ и забираем move'ом в prev_ (вызывающий передаёт rvalue) —
    // без копии всего вектора строк на каждый кадр.
    std::string diff(std::vector<std::string> lines);

    // Следующий diff() вернёт полный кадр: старт, возврат после overlay,
    // пропавший кадр.
    void invalidate() { valid_ = false; }

private:
    std::vector<std::string> prev_;
    bool valid_ = false;
};

}  // namespace atmsim
