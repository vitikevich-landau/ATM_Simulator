//  реплики экрана (этап 2 feature/scene).
//
//  Это ЧИСТЫЕ ДАННЫЕ: статические таблицы строк UTF-8 и функции выбора по
//  ключу. Ни времени, ни состояния, ни завиcимостей от движка — поэтому
//  файл легко проверяется тестами и безопасен для любых потоков.
//
//  Все глифы спрайтов обязаны входить в белый список GlyphSet (одна колонка
//  на кодовую точку) — это закреплено тестом sprites_use_only_allowed_glyphs.
// ============================================================================
#include <array>
#include <string>
#include <vector>

#include "atmsim/engine/ServiceStages.hpp"  // ServiceStage

namespace atmsim::scene {

// Поза человечка (3 строки × 3 колонки, пробелы прозрачны при blit).
// Этап 2 — статичные позы; этапы 3–4 добавят кадры походки, idle-вариации
// и акты по этапам обслуживания.
enum class ActorPose {
    Stand,      // стоит (руки по швам, ноги врозь)
    ReachLeft,  // тянется влево (к банкомату, который слева от клиента)
    WalkA,      // шаг: ноги врозь
    WalkB,      // шаг: ноги вместе (кадры чередуются по позиции актёра)
};

// Строки позы. Возвращает ссылку на статическую таблицу — копий нет.
const std::vector<std::string>& poseRows(ActorPose pose);

// Корпус банкомата: kAtmArtRows строк по kAtmArtWidth колонок. Рисуется
// НЕПРОЗРАЧНО (через text), чтобы вырезать себе место на любом фоне; экран
// и индикаторы накладываются поверх отдельно (см. SceneComposer).
constexpr int kAtmArtWidth = 16;
constexpr int kAtmArtRows = 9;
const std::vector<std::string>& atmArtRows();

// «Реплика» экрана банкомата для этапа обслуживания: две строки не длиннее
// kAtmScreenWidth колонок каждая. Формулировки — от лица БАНКОМАТА (он
// обращается к клиенту), в отличие от to_string(ServiceStage), где действия
// описываются от третьего лица для таблицы.
constexpr int kAtmScreenWidth = 12;
std::array<std::string, 2> screenStageText(ServiceStage stage);

}  // namespace atmsim::scene
