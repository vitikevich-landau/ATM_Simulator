#pragma once
// ============================================================================
//  ActorAnim.hpp — детерминированный выбор поз актёров (этап 4 feature/scene).
//
//  Ключевая идея: НИКАКОГО RNG-состояния. Всё «случайное» в поведении
//  человечков (личная фаза переминания, редкие взмахи рукой) вычисляется
//  хэшем splitmix64 от id клиента и номера временного окна. Одна и та же
//  пара (id, момент времени) всегда даёт одну и ту же позу:
//    * кадры воспроизводимы побайтно — golden-тесты не флапают;
//    * у каждого актёра своя фаза — очередь не машет руками синхронно;
//    * состояния между кадрами хранить не нужно.
//
//  Время сюда приходит тем же параметром nowSec, что и в ScenePresenter::tick
//  (рендер-часы). Это КОСМЕТИЧЕСКОЕ время: этап обслуживания оно не двигает
//  (этап авторитетно приходит из снимка движка) — поэтому пауза симуляции
//  оставляет человечков «живыми», но ничего смыслового не меняет.
// ============================================================================
#include <cstdint>
#include <optional>

#include "atmsim/console/scene/SceneSprites.hpp"  // ActorPose
#include "atmsim/core/Types.hpp"                  // ClientId
#include "atmsim/engine/ServiceStages.hpp"        // ServiceStage

namespace atmsim::scene {

// Финализатор splitmix64 — быстрый качественный хэш 64->64 бит.
std::uint64_t splitmix64(std::uint64_t x);

// Поза стоящего в очереди: неспешное переминание с личной фазой; изредка
// (детерминированно, примерно раз в 40 секунд на актёра) — взмах рукой.
ActorPose pickIdlePose(ClientId id, double tSec);

// Поза нервничающего (терпение на исходе): частое переминание с ноги на ногу.
ActorPose pickNervousPose(ClientId id, double tSec);

// Поза обслуживаемого у банкомата. «Ручные» этапы (вставить карту, PIN,
// сумма, забрать наличные...) — рука ходит к панели и обратно; «машинные»
// (связь с банком, отсчёт купюр, печать чека...) — клиент ждёт и переминается.
ActorPose pickActPose(ClientId id, std::optional<ServiceStage> stage, double tSec);

// Личный ТЕМП ходьбы актёра: множитель к базовой скорости сцены, 0.75..1.35.
// Детерминирован от id (свой «сольник», чтобы не коррелировать с фазой
// idle-циклов): кто-то семенит, кто-то вышагивает — очередь и уходящие
// перестают двигаться строем. НЕ применяется к подходу к банкомату: его темп
// диктует движок (approachProgress из снимка).
double walkSpeedFactor(ClientId id);

// --- Болтовня соседей по очереди ---------------------------------------------
// Скучающие соседи по очереди время от времени разговаривают: сближаются на
// клетку, жестикулируют по очереди, над говорящим — пузырёк речи. Всё, как и
// остальная «жизнь», — чистые функции времени и id: никакого состояния,
// продвижение очереди разрывает пару само (стадии Advance).

// Идёт ли СЕЙЧАС эпизод болтовни у пары соседей и кто говорит. Время (со
// сдвигом личной фазы пары) делится на окна ~9 c; хэш (пара, номер окна) даёт
// шанс эпизода 1/3; эпизод занимает первые ~3.5 c окна, роль говорящего
// меняется каждые ~1.7 c.
enum class ChatRole { None, LeftSpeaks, RightSpeaks };
ChatRole chatState(ClientId left, ClientId right, double tSec);

// Поза участника болтовни: говорящий жестикулирует (взмахи ~2 Гц с личной
// фазой), слушающий стоит и изредка переминается.
ActorPose pickChatPose(ClientId id, bool speaking, double tSec);

// Пузырёк речи над головой говорящего: «пульсирующая» точка · ○ ● (~3 кадра/c).
// Все три глифа — из белого списка GlyphSet.
char32_t chatBubble(double tSec);

}  // namespace atmsim::scene
