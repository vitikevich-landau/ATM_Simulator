#pragma once
// ============================================================================
//  GlyphSet.hpp — белый список глифов анимированной сцены + UTF-8 хелперы.
//
//  ЗАЧЕМ ЭТО НУЖНО. Вся раскладка дашборда держится на инварианте
//  «1 кодовая точка = 1 колонка терминала»: fit() в LiveRenderer.cpp считает
//  ширину по кодовым точкам, а не по реальным столбцам. Любой глиф шириной
//  2 колонки (эмодзи, CJK) молча ломает выравнивание и позиционирование
//  спрайтов — причём по-разному в Windows Terminal и legacy conhost.
//  Поэтому сцена рисует ТОЛЬКО глифы из этого белого списка: ASCII, кириллица,
//  box-drawing, блочные элементы и небольшой набор узких знаков. Эмодзи,
//  braille, комбинируемые и ZWJ-последовательности запрещены навсегда.
//
//  Слой контроля двойной: в debug-сборке запрещённый глиф роняет assert
//  (разработчик сразу видит ошибку в спрайте), в release — молча заменяется
//  на '?' (сцена чуть некрасивая, но раскладка цела).
// ============================================================================
#include <string>
#include <string_view>

namespace atmsim::scene {

// Входит ли кодовая точка в белый список одноколоночных глифов сцены.
bool isAllowedGlyph(char32_t cp);

// Допустимый глиф возвращается как есть, запрещённый — заменяется на '?'.
// Отдельная функция (а не приватная деталь put), чтобы покрыть тестом путь
// release-замены, не роняя debug-assert самого put().
char32_t sanitizeGlyph(char32_t cp);

namespace utf8 {

// Декодирует UTF-8 строку в последовательность кодовых точек. Битые байты
// не роняют программу, а превращаются в U+FFFD (дальше sanitizeGlyph сведёт
// их к '?') — канва обязана переживать любые входные данные.
std::u32string decode(std::string_view s);

// Дописывает кодовую точку к строке в виде байтов UTF-8.
void append(std::string& out, char32_t cp);

}  // namespace utf8
}  // namespace atmsim::scene
